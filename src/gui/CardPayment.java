    private void pagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pagarActionPerformed
        // TODO add your handling code here:
        ArrayList<CardDetails> aux = user.getCards();
        user.pay(aux.get(cardList.getSelectedIndex()), pur);
        User owner = pur.getProduct().getProductOwner();
        owner.addSelledHist(pur);
        user.sellPaid(pur.getProduct());
        checkWindow.showAllCategories();
        checkWindow.showAllProducts();
        checkWindow.showBoughtProducts();
        checkWindow.showOngoingPurchase();
        checkWindow.showSoldProducts();
        checkWindow.setVisible(true);
        this.setVisible(false);
    }//GEN-LAST:event_pagarActionPerformed

